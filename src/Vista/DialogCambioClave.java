    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         VendedorDAO vd = new VendedorDAO();
         
        if (txtcn.getText() == txtconfirmacion.getText()){
            if (vd.cambiarContraseña(txtca.getText(), user, txtcn.getText())!=0){
                JOptionPane.showMessageDialog(null,"Contraseña modificada correctamente");
            }else{
                JOptionPane.showMessageDialog(null,"Contraseña no pertenece a este usuario");
            }     
        }else{
            JOptionPane.showMessageDialog(null, "Contraseñas no coinciden");
        }
         
    }//GEN-LAST:event_jButton1ActionPerformed

