    public void validar(){
        String dni = txtPass.getText();
        String user = usuario.getText();
        
        if (dni.equals("") || user.equals("")){
            JOptionPane.showMessageDialog(null, "Debe ingresar datos en las cajas de Texto");
            usuario.requestFocus();
        }else{
            ve = vd.ValidarVendedor(dni, user);
            if(ve.getUser() != null && ve.getDni() != null){
                Principal p = new Principal();
                p.setVisible(true);
                dispose();
                
            }else{
                JOptionPane.showMessageDialog(null, "Debe ingresar usuarios validos");
                usuario.requestFocus();   
            }
        }
    }
